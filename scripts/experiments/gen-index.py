#!/usr/bin/env python3
"""Render the EXPERIMENTS.md generated index + YAML header links (ADR-004.2).

Mirrors the definitions-generator pattern (scripts/rabbitmq/generate-definitions.py):
the YAML files under experiments/ are the source of truth; this script renders a
derived view into EXPERIMENTS.md and is idempotent (re-run → no diff).

Two edits, both idempotent:
 1. Replace the table between the HTML markers
    <!-- experiments-index:begin --> … <!-- experiments-index:end -->
    (inserted before "## Before you start" if absent) with a row per
    experiments/exp-*.yaml: id, title, needs, scored?, link to the YAML.
 2. Canonicalize every "## EXP-NN · …" header that has a matching YAML into
    "## [EXP-NN · <title>](experiments/exp-NN.yaml)" — a header that is a link
    keeps the same GitHub anchor, so cross-references survive.

Run: python3 scripts/experiments/gen-index.py   (add --check to fail on drift)
"""
from __future__ import annotations

import re
import sys
from pathlib import Path

import yaml

ROOT = Path(__file__).resolve().parent.parent.parent
EXPERIMENTS_DIR = ROOT / "experiments"
DOC = ROOT / "EXPERIMENTS.md"

BEGIN = "<!-- experiments-index:begin -->"
END = "<!-- experiments-index:end -->"
GENERATED_NOTE = "<!-- GENERATED by scripts/experiments/gen-index.py from experiments/*.yaml — do not edit by hand. -->"
HEADER_RE = re.compile(r"^## \[?(EXP-(\d+))\b.*$", re.MULTILINE)


def github_slug(text: str) -> str:
    """GitHub heading anchor: lowercase, drop non [a-z0-9 -], spaces → hyphens."""
    s = text.strip().lower()
    s = re.sub(r"[^\w \-]", "", s, flags=re.UNICODE).replace("_", "")
    return s.replace(" ", "-")


def load_experiments() -> list[dict]:
    out = []
    for path in sorted(EXPERIMENTS_DIR.glob("exp-*.yaml"),
                       key=lambda p: int(re.search(r"(\d+)", p.stem).group(1))):
        doc = yaml.safe_load(path.read_text()) or {}
        out.append({
            "id": doc.get("id", path.stem),
            "num": path.stem.split("-", 1)[1],
            "title": doc.get("title", ""),
            "needs": doc.get("needs") or [],
            "scored": bool(doc.get("assertions")),
            "rel": f"experiments/{path.name}",
        })
    return out


def render_index(experiments: list[dict]) -> str:
    rows = [
        "| Experiment | Title | Needs | Scored | Definition |",
        "| --- | --- | --- | --- | --- |",
    ]
    for e in experiments:
        disp = e["id"].upper()
        anchor = github_slug(f"{disp} · {e['title']}")
        needs = ", ".join(f"`{n}`" for n in e["needs"]) or "—"
        scored = "yes" if e["scored"] else "no"
        rows.append(
            f"| [{disp}](#{anchor}) | {e['title']} | {needs} | {scored} | "
            f"[{Path(e['rel']).name}]({e['rel']}) |"
        )
    table = "\n".join(rows)
    return f"{BEGIN}\n{GENERATED_NOTE}\n\n{table}\n\n{END}"


def apply_index(text: str, block: str) -> str:
    if BEGIN in text and END in text:
        return re.sub(re.escape(BEGIN) + r".*?" + re.escape(END), lambda _: block,
                      text, count=1, flags=re.DOTALL)
    anchor = "## Before you start"
    insert_at = text.find(anchor)
    if insert_at == -1:
        insert_at = text.find("\n## EXP-")
        insert_at = insert_at + 1 if insert_at != -1 else len(text)
    return text[:insert_at] + block + "\n\n" + text[insert_at:]


def apply_header_links(text: str, by_num: dict[str, dict]) -> str:
    def repl(m: re.Match) -> str:
        num = m.group(2)
        e = by_num.get(num)
        if not e:  # no YAML (e.g. EXP-20..34) — leave the header untouched
            return m.group(0)
        return f"## [{e['id'].upper()} · {e['title']}]({e['rel']})"
    return HEADER_RE.sub(repl, text)


def main() -> int:
    check = "--check" in sys.argv[1:]
    experiments = load_experiments()
    by_num = {e["num"]: e for e in experiments}

    original = DOC.read_text()
    text = apply_index(original, render_index(experiments))
    text = apply_header_links(text, by_num)

    if text == original:
        print(f"EXPERIMENTS.md up to date ({len(experiments)} experiments indexed)")
        return 0
    if check:
        print("EXPERIMENTS.md is stale — run `python3 scripts/experiments/gen-index.py`",
              file=sys.stderr)
        return 1
    DOC.write_text(text)
    print(f"EXPERIMENTS.md updated ({len(experiments)} experiments indexed)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
